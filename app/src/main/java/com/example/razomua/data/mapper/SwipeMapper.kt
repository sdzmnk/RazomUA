package com.example.razomua.data.local.mapper

import com.example.razomua.data.local.entity.SwipeEntity
import com.example.razomua.model.Swipe

fun Swipe.toEntity() = SwipeEntity(
    id = id,
    fromUserId = fromUserId,
    toUserId = toUserId,
    action = action,
    createdAt = createdAt
)

